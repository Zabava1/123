a = float(input('Сколько километров спортсмен пробежал в первый день? '))
b = float(input('Какого результата вы ждете от него? (количество километров) '))
i = 0

while a < b:
    a = a * 1.1
    i += 1

print(f'На {i} день спорцмен достигнет результата {a:.1f} километров')

